namespace SixLabors.Fonts;

/// <summary>
/// Specifies the writing direction for text.
/// </summary>
public enum TextDirection
{
    /// <summary>
    /// Left to right.
    /// </summary>
    LeftToRight = 0,

    /// <summary>
    /// Right to left.
    /// </summary>
    RightToLeft = 1,

    /// <summary>
    /// Automatically determined.
    /// </summary>
    Auto = 2,
}
